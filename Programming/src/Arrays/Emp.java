package Arrays;

public class Emp {
	
	String name;
	int id;
	int salary;
	
	public Emp(String name, int id, int salary) {

		this.name = name;
		this.id = id;
		this.salary = salary;
	}

	@Override
	public String toString() {
		return "Emp [name=" + name + ", id=" + id + ", salary=" + salary + "]";
	}
	
	
	
	

	

	}


